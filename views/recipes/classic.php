        <label for="categorie" class="text"></label>
        <h2>
            <p class="card-text">
                <p>Son origine...</p>
        </h2>

        <label for="description"></label>
        <p class="card-text text">
            <p>La ficelle picarde a été créée dans les années 1950 à Amiens,<br>
                par le cuisinier Marcel Lefèvre1. Durant la foire exposition de la Hotoie à Amiens,<br>
                un repas était servi aux notables du département. Les restaurateurs réputés de la ville étaient invités
                à l'élaboration du menu,<br>
                et c'est à cette occasion qu'est née la ficelle picarde. <br>

                La recette originale de la ficelle picarde est protégée par la Confrérie des compagnons de la ficelle
                picarde.</p>
            <br>
            <h2>
                <p class="card-text">
                    <p>La recette.</p>
            </h2>

            <label for="ingredient"></label>
            <p class="card-text text">
                <p>
                    Pour la pâte à crêpes : 150g de farine, 2 œufs, 30 g de beurre, 25 cl de lait, sel <br>

                    Pour la garniture : 300g de champignons de Paris, 6 tranches fines de jambon blanc, 2 échalotes,<br>
                    150 g de gruyère râpé, 20 cl. de crème fraîche épaisse, 1/2 citron, sel, poivre créme fraiche.

                    <label for="step"></label>
                    <p class="card-text text">
                        <p>Préparation de la pâte à crêpes : faites fondre le beurre à feu doux. laissez tiédir. Versez
                            la farine en tas dans un saladier. Ajoutez le sel. <br>
                            Creusez un puits et cassez les 2 œufs. Tournez en partant du centre et en faisant tomber peu
                            à peu la farine dans les œufs. Délayez avec le lait froid. <br>
                            Ajoutez enfin le beurre fondu. La pâte obtenue doit être lisse et sans grumeaux. Couvrez et
                            laissez-la reposer 30 minutes. <br>
                            Pour avoir une pâte plus légère, remplacez 1/4 du lait par de la bière blonde que vous
                            mélangerez à la pâte juste avant de cuire les crêpes. <br><br>

                            Coupez le bout terreux des champignons, lavez-les, essuyez-les et coupez-les en petits
                            morceaux. Pelez et hachez finement les échalotes. <br>
                            Faites-les revenir dans une poêle avec 25 g de beurre sur feu doux pendant 3 minutes, puis
                            ajoutez les champignons. <br>
                            Arrosez d’un filet de citron. Laissez cuire 8 à 10 minutes en remuant souvent. En fin de
                            cuisson, toute l’eau de végétation rendue par les champignons doit être évaporée. <br>
                            Salez, poivrez. Ajoutez le jambon coupé en petits morceaux et la créme, mélangez et
                            réservez.

                            Procédez à la cuisson des crêpes. Dans chacunes-d’elles, étalez dessus une couche de la
                            préparation aux champignons, puis roulez les crêpes comme un cigare en serrant bien. <br>
                            Disposez-les dans un plat à gratin en les rangeant les unes à côté des autres. Nappez-les de
                            20 cl de crème fraîche. <br>
                            Poivrez et parsemez de gruyère râpé. Faites gratiner pendant 5 à 6 minutes sous le gril.
                            Servez immédiatement. </p>
