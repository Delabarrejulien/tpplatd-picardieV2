    <div class="card-body text">
       <h1 class="card-title text">Pommes Riboches</h1>

       <label for="categorie" class="text"></label>
       <h2><p class="card-text"><p>Le déssert</p></h2>

       <label for="description"></label>
       <p class="card-text text"><p>Du sucre pour les gourmands !</p>

       <label for="ingredient"></label>
       <p class="card-text text"><p>
      6 pommes reinettes étoilée ou reine des reinettes de taille moyenne 
      <br>
      500 g de pâte feuilletée 
      <br>
      3 c. à s. de sucre semoule 
      <br>
      3 c. à s. de cassonade 
      <br>
      2 c. à c. de cannelle en poudre 
      <br>
      1 jaune d’oeuf

       <label for="step"></label>
       <p class="card-text text"><p>
      Dans un bol, mélanger le sucre, la cassonade et la cannelle.
      Eplucher et évider les pommes, puis rouler dans le mélange sucres-cannelle.
      Couvrir de film plastique et mettre au réfrigérateur durant 1h.
      Préchauffer le four à 200° C .
      Abaisser la pâte sur 3 à 4 mm d’épaisseur, puis couper en carrés de 10 cm de côté.
      Humidifier les bords et placer une pomme au centre.
      Tenir les pointes de façon à ce qu’elles remontent vers le centre.
      Sceller la pâte et éventuellement façonner quatre feuilles en pâte feuilletée et faire partir du dessus.
      Dorer à l’oeuf délayé avec une cuillère à soupe d’eau froide.
      poser sur une plaque de cuisson munie de papier sulfurisé.
      Enfourner durant 10 min.
      Dorer de nouveau et enfourner 20 à 25 min supplémentaires.
      Servir tiède.
      Déguster .</p>
       <p class="card-text">
          <small class="text-muted text">Bon apppétit...</small>
       </p>
