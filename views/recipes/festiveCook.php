       <h1 class="card-title text">Souris d'agneau des prés salés confite en papillote à l'ail et thym ...</h1>

       <label for="categorie" class="text"></label>
       <h2><p class="card-text"><p>Plat de résistance</p></h2>

       <label for="description"></label>
       <p class="card-text text"><p>A chacun sa petite papillote !</p>

       <label for="ingredient"></label>
       <p class="card-text text"><p>
      1 souris d'agneau ( les miennes étaient énormes )
      <br>

      3 branches de thym ou des herbes de Provence
      <br>
      3 gousses d'ail non épluchées ( au moins 3 par papillotes )
      <br>
      sel et poivre du moulin
      <br>
      1 c à soupe d'huile de tournesol</p>

       <label for="step"></label>
       <p class="card-text text"><p>réchauffez le four à 240 °
      <br>
       Placez devant soi, une feuille d'alu sur laquelle je place une grande feuille de papier de cuisson 
      <br>
       pour pouvoir envelopper correctement la souris d'agneau avec les pommes de terre.
       <br>
       Au centre de la feuille de papier de cuisson, mettez votre souris d'agneau et entourez-la de 3 pommes de terre crues et de taille moyennes,
       <br>
       ajouter les 3 gousses d'ail non épuchées, les branches de thym, bien saler et poivrer, ajouter 1 c à soupe d'huile d'olive ou autre ..
      <br>
      bien fermer la feuille de cuisson, comme un bonbon mais je replis les côtés sur le dessous ... puis la feuille d'alu.
      <br>
      Enfournez pour 2 heures 30 au moins à 240° , puis ouvrir la papillote 30 minutes avant la fin de la cuisson.</p>
       <p class="card-text">
          <small class="text-muted text">Bon apppétit...</small>
       </p>
