       <h1 class="card-title text">Oeufs cocotte au maroilles et maquereau fumé</h1>

       <label for="categorie" class="text"></label>
       <h2><p class="card-text"><p>En entrée</p></h2>

       <label for="description"></label>
       <p class="card-text text"><p>c'est plus léger qu'il n'y paraît...</p>

       <label for="ingredient"></label>
       <p class="card-text text"><p>
       4 œufs 
       <br>
      110 g de maroilles 
      <br>
      1 filet de maquereau fumé 
      <br>
      20 cl de crème épaisse fermière 
      <br>
      7 cl de lait entier 
      <br>
      1 pincée de cumin en poudre  
      <br>
      4 cuil à soupe de ciboulette hachée  
      <br>
      2 cuil à soupe de persil haché  
      <br>
      2 branches d'estragon 
      <br>
      1 noix de beurre (pour beurrer les ramequins) 
      <br>
      sel et poivre

       <label for="step"></label>
       <p class="card-text text"><p>
       Préchauffez le four à 210° C. Faites fondre le maroilles dans le lait et la crème épaisse. Ajoutez le sel, le poivre et le cumin.
       Beurrez 4 ramequins. Dans chaque ramequin, ajoutez la préparation à base de maroilles ainsi qu'un œuf. 
       Placez au four et cuisez 12 min. Ajoutez pour finir, la ciboulette hachée, le persil haché, les feuilles d'estragon ainsi que le maquereau fumé effiloché.
       Déguster .</p>
       <p class="card-text">
          <small class="text-muted text">Bon apppétit...</small>
       </p>
